        <h2 class="pt-2 text-lg font-bold">Que hacer en caso de un accidente:</h2>
        <h3 class="pt-2 text-lg font-bold underline underline-offset-4 ...">
          Accidente de Trabajo o Enfermedad Profesional
        </h3>
        <p>
          Se considera accidente de trabajo todo hecho súbito y violento que
          ocurra por el hecho o en ocación del trabajo, como así también los
          ocurridos entre el domicilio del trabajador y el lugar del trabajo,
          siempre y cuando el trabajador no modifique o altere el trayecto por
          causas ajenas al trabajo. <br />
          <br />
          Se consideran enfermedades profesionales a aquellas que se producen
          como consecuencia de la realización de sus tareas laborales.
        </p>

        <h2 class="pt-2 text-lg font-bold underline underline-offset-4 ...">Denuncia de Accidente</h2>
        <p>
          El empleador está obligado a denunciar a la Aseguradora, todo
          accidente de trabajo o enfermedad profesional que sufran sus
          trabajadores. También podrá efectuar la denuncia el propio trabajador,
          sus derechohabientes o cualquier persona que haya tenido conocimiento
          del accidente de trabajo o enfermedad profesional. <br />
          <br />
          La denuncia estará dirigida a la Aseguradora, pero podrá ser
          presentada ante el prestador de servicios que aquella habilite a tal
          fin. <br />
          <br />
          Cuando la denuncia se presente directamente ante la Aseguradora, ésta
          deberá tomar los recaudos necesarios para que el trabajador reciba en
          forma inmediata las prestaciones en especie. <br />
          <br />
          En el caso de que el trabajador, sus derechohabientes o cualquier
          persona que haya tenido conocimiento del accidente de trabajo
          presentara la denuncia por escrito ante la ART, la misma deberá
          contener una relación de los hechos, la identificación de las partes
          (Trabajador / Empleador) y la firma del denunciante.
        </p>
